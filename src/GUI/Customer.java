    private void tampildataCustomer(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nama");
        model.addColumn("Nomor KTP");
        model.addColumn("Nomer Telp");
        model.addColumn("Jenis Kelamin");
        model.addColumn("Alamat");
        tbl_customer.setModel(model);
        try {            
            String sql = "SELECT * FROM customer";
            java.sql.Connection conn=(Connection)Services.KoneksiDB.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while (res.next()) {                
                Object[] obj = new Object[5];
                obj[0] = res.getString("nama_customer"); 
                obj[1] = res.getString("nomor_ktp");
                obj[2] = res.getString("notelp");
                obj[3] = res.getString("jenkel");
                obj[4] = res.getString("alamat");
                
                model.addRow(obj);
            }
            tbl_customer.setModel(model);
            //s.close();
            stm.close();
        } catch (SQLException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

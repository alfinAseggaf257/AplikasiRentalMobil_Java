    private void tbl_customerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_customerMouseClicked
        int baris = tbl_customer.rowAtPoint(evt.getPoint());
        String nama_customer =tbl_customer.getValueAt(baris, 0).toString();
        txt_nama_customer.setText(nama_customer);
        String nomor_ktp = tbl_customer.getValueAt(baris,1).toString();
        txt_nomor_ktp.setText(nomor_ktp);
        String notelp = tbl_customer.getValueAt(baris, 2).toString();
        txt_notelp.setText(notelp);
        String jenkel = tbl_customer.getValueAt(baris, 3).toString();
        cmb_jenkel.setSelectedItem(jenkel);
        String alamat=tbl_customer.getValueAt(baris, 4).toString();
        txta_alamat.setText(alamat);    
    }//GEN-LAST:event_tbl_customerMouseClicked


    private void tbl_pengembalianMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_pengembalianMouseClicked
        int baris = tbl_pengembalian.rowAtPoint(evt.getPoint()); 
        String tanggal =tbl_pengembalian.getValueAt(baris, 2).toString();
        try {
            java.util.Date date2 = new SimpleDateFormat("yyyy-MM-dd").parse(tanggal);
            dd_tanggal_pengembalian.setDate(date2);
        } catch (ParseException ex) {
            Logger.getLogger(Peminjaman.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String kode_pengembalian =tbl_pengembalian.getValueAt(baris, 0).toString();
        txt_kode_pengembalian.setText(kode_pengembalian);
        String kode_peminjaman = tbl_pengembalian.getValueAt(baris, 1).toString();
        cmb_kode_peminjaman.setSelectedItem(kode_peminjaman);
        String status =tbl_pengembalian.getValueAt(baris, 3).toString();
        txt_status.setText(status);
    }//GEN-LAST:event_tbl_pengembalianMouseClicked


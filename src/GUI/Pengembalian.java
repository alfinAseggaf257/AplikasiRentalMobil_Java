    public void tampildataPengembalian(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Pengembalian");
        model.addColumn("Kode Peminjaman");
        model.addColumn("Tanggal");
        model.addColumn("Status Pengembalian");
        tbl_pengembalian.setModel(model);
        try {            
            String sql = "SELECT * FROM pengembalian";
            java.sql.Connection conn=(Connection)Services.KoneksiDB.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while (res.next()) {                
                Object[] obj = new Object[4];
                obj[0] = res.getString("kode_pengembalian"); 
                obj[1] = res.getString("kode_peminjaman"); 
                obj[2] = res.getString("tanggal_pengembalian");
                obj[3] = res.getString("status");
                
                model.addRow(obj);
            }
            stm.close();
        } catch (SQLException ex) {
            Logger.getLogger(Mobil.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
     public void tampil_combo_Peminjaman()
    {
        try {            
            String sql = "SELECT kode_peminjaman FROM peminjaman";
            java.sql.Connection conn=(Connection)Services.KoneksiDB.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while (res.next()) {                
                Object[] obj = new Object[3];
                obj[0] = res.getString(1); 
                
                cmb_kode_peminjaman.addItem((String) obj[0]);   
            }
            
            res.close();
            stm.close();
        } catch (SQLException ex) {
            Logger.getLogger(Pengembalian.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


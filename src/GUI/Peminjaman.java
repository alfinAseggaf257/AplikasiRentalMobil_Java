    private void tampildataPeminjaman(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Peminjaman");
        model.addColumn("Nama Peminjam");
        model.addColumn("Nomor KTP");
        model.addColumn("Merk Mobil");
        model.addColumn("Nopol");
        model.addColumn("Tanggal");
        model.addColumn("Durasi");
        model.addColumn("Total Bayar");
        tbl_peminjaman.setModel(model);
        try {            
            String sql = "SELECT * FROM peminjaman "
                    + " INNER JOIN customer ON peminjaman.nomor_ktp=customer.nomor_ktp"
                    + " INNER JOIN mobil ON peminjaman.nopol_mobil=mobil.nopol_mobil";
     
            java.sql.Connection conn=(Connection)Services.KoneksiDB.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while (res.next()) {                
                Object[] obj = new Object[8];
                obj[0] = res.getString("kode_peminjaman"); 
                obj[1] = res.getString("nama_customer"); 
                obj[2] = res.getString("nomor_ktp");
                obj[3] = res.getString("merk_mobil");
                obj[4] = res.getString("nopol_mobil");
                obj[5] = res.getString("tanggal");
                obj[6] = res.getString("durasi_sewa");
                obj[7] = res.getString("total_bayar");
                
                model.addRow(obj);
            }
            stm.close();
        } catch (SQLException ex) {
            Logger.getLogger(Mobil.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

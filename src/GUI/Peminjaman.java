    private void txt_durasi_sewaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_durasi_sewaKeyReleased
        // TODO add your handling code here:
        int harga = Integer.parseInt(txt_harga.getText());
        int durasi = Integer.parseInt(txt_durasi_sewa.getText());
        int potongan;
        if(durasi >=3 && durasi <7){
            txt_potongan_harga.setText("30000");
            potongan = 30000;
        }else if(durasi>=7 && durasi < 10){
            txt_potongan_harga.setText("50000");
            potongan = 50000;
        }else if(durasi>=10){
            txt_potongan_harga.setText("70000");
            potongan = 70000;
        }else{
            txt_potongan_harga.setText("0");
            potongan = 0;
        }
        int total_bayar = (( harga * durasi) - potongan);
        txt_total_bayar.setText(Integer.toString(total_bayar));
    }//GEN-LAST:event_txt_durasi_sewaKeyReleased

